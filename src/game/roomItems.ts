function makeItem(id: string, name: string, file: string): RoomItemDef {
  return { id, name, art: `${ASSET_BASE}assets/room-items/${file}` };
}

function nameFor(id: string): string {
  const key = id.replace(/^item_\d+_/, '');
  return NAME_BY_KEY[key] ?? key;
}
