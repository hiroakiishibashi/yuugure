const originalItems = ORIGINAL_ITEM_FILES.map((file) => {
  const id = file.replace(/\.png$/, '');
  return makeItem(id, nameFor(id), file);
});

const legacyItems = LEGACY_ITEMS.map(([id, name, file]) => makeItem(id, name, file));

export const ROOM_ITEMS: readonly RoomItemDef[] = [...legacyItems, ...originalItems];

export const ROOM_ITEM_IDS: readonly string[] = ROOM_ITEMS.map((i) => i.id);

const BY_ID = new Map(ROOM_ITEMS.map((i) => [i.id, i]));

export function getRoomItem(id: string): RoomItemDef | undefined {
  return BY_ID.get(id);
}

